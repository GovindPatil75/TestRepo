package com.Practice;

public class Looping_for {

	public static void main(String[] args) {
		
		
		for(int i=0;i<=5;i++) { //1<=5 2<=5 3<=5  4<=5  5<=5 6<=5
			
			System.out.print(i);  //0 1 2 3 4 5
		}
		
		System.out.println("Test");

		
		for(int j=0;j<=5;j++) {
			
			System.out.println("*");
		}
	}

}
